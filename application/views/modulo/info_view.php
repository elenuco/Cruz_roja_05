            <div class="col-md-9 card card-signing" class="bg-ligth"> <br>
               <center><p><strong>El objetivo de ella, según el artículo primero, es “Socorrer a los enfermos y heridos militares, de cualquiera de las partes beligerantes, ya sea en el campo de batalla o en los hospitales del ejercito”. El 28 de marzo de 1885, apenas fundada la Sociedad Nacional. El Salvador entro en conflicto con Guatemala, al oponerse a la política Federalista del presidente de se país, Justo Rufino Barrios y firmar con Nicaragua y Costa Rica una alianza para defender con las armas la independencia de las 3 repúblicas. Ese conflicto, en cierto modo, el bautismo de fuego de la Cruz Roja Salvadoreña, que organizo servicios de camilleros e improviso hospitales, donde se hicieron famosos los doctores Juan Padilla Matute y Herna Power. La Sociedad Nacional sitio a las tropas salvadoreñas y en algunos casos, socorrió a los heridos y enfermos del ejército de Guatemala.<strong></p></center>
             </div>
              <br> <br>
              <div class="col-md-9 card card-signing" class="bg-ligth"><br>
                <center><p><strong>A comienzos del siglo, Cruz Roja Salvadoreña se asocio al Consejo Superior de Salubridad e inicio sus actividades en tiempo de paz con la lucha contra las epidemias del cólera, viruela, meningitis, gripe y disentería  que se registraron tras los enfrentamientos entre El Salvador y Guatemala en 1906 y Nicaragua después en 1907. Todo esto ya bajo la presidencia de Don Miguel Yudice, quien supo mantener el accionar de la institución en forma permanente. Se formo la Asociación de Damas, que se convirtió en el Comité de Damas Voluntarias en 1952.  En 1917 el Consejo Supremo decidió reorganizar la Sociedad Nacional de acuerdo con las disposiciones del Primer Convenio de Ginebra de 1864 ya que El Salvador fue el primer país del continente Americano en adherirse en el año de 1874 fungiendo como Presidente de la Republica el Mariscal de Campo don Santiago Gonzalez. Esta decisión se tradujo en la aprobación de nuevos estatutos el 5 de julio de 1918. El 25 de abril de 1925, el CICR, reconoció oficialmente a la Cruz Roja Salvadoreña, incorporándose también a la  Liga, hoy Federación el 24 de junio en ese mismo año.<strong></p></center>
